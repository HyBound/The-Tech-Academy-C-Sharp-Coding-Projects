using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace IntegeratorProgram
{
    class Math
    {
        public int Add10(int number)
        {
            int adder = number + 10;
            return adder;
        }

        public int Mult10(int number)
        {
            int mult = number * 10;
            return mult;
        }

        public int Div10(int number)
        {
            int div = number / 10;
            return div;
        }
    }
}
